export function findCountryByFrenchCode(code: string): FrenchDiplomaticCode | null {
  const cleanCode = code.trim()
  console.log("üîç Recherche du pays pour le code:", cleanCode)

  const result =
    frenchDiplomaticCodes.find((country) => {
      const match = country.code === cleanCode || country.alternativeCode === cleanCode
      if (match) {
        console.log("‚úÖ Pays trouv√©:", country.countryName, "pour le code", cleanCode)
      }
      return match
    }) || null

  if (!result) {
    console.log("‚ùå Aucun pays trouv√© pour le code:", cleanCode)
  }

  return result
}

export function getFrenchDiplomaticCodesByRegion(): Record<string, FrenchDiplomaticCode[]> {
  return frenchDiplomaticCodes
    .filter((country) => country.status === "active")
    .reduce(
      (acc, country) => {
        if (!acc[country.region]) {
          acc[country.region] = []
        }
        acc[country.region].push(country)
        return acc
      },
      {} as Record<string, FrenchDiplomaticCode[]>,
    )
}
